package DAy2;

public class C05_preIncrement_postIncrement {
    public static void main(String[] args) {


        int x= 20;

        System.out.println(x++);//20


        int y= ++x; //22


        System.out.println(--y);// 21


        int z= --y; //20


        System.out.println(z++); //20


        System.out.println(z); //21



    }
}
